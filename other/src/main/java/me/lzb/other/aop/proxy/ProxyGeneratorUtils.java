package me.lzb.other.aop.proxy;

import sun.misc.ProxyGenerator;

import java.io.FileOutputStream;
import java.io.IOException;

public class ProxyGeneratorUtils {

    public static void main(String[] args) {
        byte[] classFile = ProxyGenerator.generateProxyClass("$Proxy100", HelloWorld.class.getInterfaces());

        FileOutputStream out = null;

        try {
            out = new FileOutputStream("D:\\code\\learning\\tmp\\$Proxy100.class");
            out.write(classFile);
            out.flush();
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                out.close();
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }
}